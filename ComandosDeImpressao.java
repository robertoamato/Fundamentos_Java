public class ComandosDeImpressao {
  public static void main(String[] args) {
    System.out.println("Hello, World!"); // println = imprime na tela e pula linha
    System.out.print("Hello, World!"); // print = imprime na tela sem pular linha
    System.out.printf("Hello, World!"); // printf = imprime na tela e pula linha formatada pelo printf
    System.out.format("Hello, World!"); // format = imprime na tela sem pular linha formatada pelo printf

  }
}